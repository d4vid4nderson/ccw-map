export function getStateLaw(stateCode: string): StateLaw | undefined {
  return stateLaws[stateCode];
}

export function getAllStates(): StateLaw[] {
  return Object.values(stateLaws);
}

export function getPermitlessCarryStates(): StateLaw[] {
  return Object.values(stateLaws).filter((s) => s.permitlessCarry);
}

export function getStatesByPermitType(permitType: StateLaw['permitType']): StateLaw[] {
  return Object.values(stateLaws).filter((s) => s.permitType === permitType);
}
